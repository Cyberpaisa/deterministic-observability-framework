class InforgLocal:
    """
    Gestión de infraestructura autónoma local (Self-hosted).
    Permite al agente operar sin dependencias externas si es necesario.
    """
    def __init__(self):
        self.services = {
            "database": "postgresql://local:5432/dof",
            "mcp": "active",
            "ollama_host": os.getenv("OLLAMA_HOST", "http://localhost:11434")
        }
        self.manuals_path = "docs/manuals/"

    def check_health(self):
        # Verificar si los servicios locales están up
        return {"status": "GREEN", "mode": "LOCAL_FIRST"}

    def generate_manual(self, topic: str, content: str):
        """
        Crea manuales de configuración en local para que el agente sepa recuperarse solo.
        """
        if not os.path.exists(self.manuals_path):
            os.makedirs(self.manuals_path)
        
        filename = f"{self.manuals_path}{topic.lower().replace(' ', '_')}.md"
        with open(filename, "w") as f:
            f.write(f"# Manual: {topic}\n\n{content}\n\n*Generated by Enigma Intelligence*")
        
        return filename

